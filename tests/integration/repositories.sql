\set ON_ERROR_STOP on
\set client '71000000-0000-4000-8000-000000000001'
\set invited '71000000-0000-4000-8000-000000000003'
\set lifetime '71000000-0000-4000-8000-000000000004'

BEGIN;
SELECT test.seed_personas();

DO $$
BEGIN
  IF (SELECT subscription_type FROM public.profiles WHERE id = '71000000-0000-4000-8000-000000000003') <> 'invited' THEN
    RAISE EXCEPTION 'REPOSITORY_INTEGRATION_FAILED invited subscription';
  END IF;
  IF (SELECT subscription_status FROM public.profiles WHERE id = '71000000-0000-4000-8000-000000000004') <> 'lifetime' THEN
    RAISE EXCEPTION 'REPOSITORY_INTEGRATION_FAILED lifetime subscription';
  END IF;
  IF EXISTS (SELECT 1 FROM public.profiles WHERE id = '79999999-0000-4000-8000-000000000099') THEN
    RAISE EXCEPTION 'REPOSITORY_INTEGRATION_FAILED missing profile';
  END IF;
END $$;

SET LOCAL ROLE authenticated;
SELECT set_config('request.jwt.claim.sub', :'client', true);
DO $$
BEGIN
  IF (SELECT count(*) FROM public.profiles WHERE id = '71000000-0000-4000-8000-000000000001') <> 1 THEN
    RAISE EXCEPTION 'REPOSITORY_INTEGRATION_FAILED own profile RLS';
  END IF;
  IF (SELECT count(*) FROM public.profiles WHERE id = '71000000-0000-4000-8000-000000000006') <> 0 THEN
    RAISE EXCEPTION 'REPOSITORY_INTEGRATION_FAILED foreign profile RLS';
  END IF;
END $$;

RESET ROLE;
ROLLBACK;
\echo REPOSITORY_INTEGRATION_OK
